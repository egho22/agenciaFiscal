    private void cargarAutos() {
        jComboBox2.removeAllItems();
        List<Automovil> autos = new ArrayList<>();
        try {
            autos = a.ConsultarTodos();
        } catch (Exception ex) {
            Logger.getLogger(LicenciasForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Automovil at : autos) {
            jComboBox2.addItem(at);
        }
    }

    private void actualizarAuto() {
        Automovil auto = (Automovil) jComboBox2.getSelectedItem();
        if (auto == null) {
            return;
        }
        jTextField2.setText(auto.getMarca());
        jTextField3.setText(auto.getLinea());
        jComboBox1.removeAllItems();
        List<Placa> placas = new ArrayList<>();
        try {
            placas = placa.ConsultarTodos();
        } catch (Exception ex) {
            Logger.getLogger(LicenciasForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Placa pla : placas) {
            if (auto.getNumeroSerie().matches(pla.getVehiculo().getNumeroSerie())) {
                jComboBox1.addItem(pla);
            }
        }
        jTextField1.setText(auto.getColor());
        jTextField4.setText(auto.getModelo());
        jTextField5.setText(auto.getTipoVehiculo());
        Placa placaSeleccionada = (Placa) jComboBox1.getSelectedItem();
        if (placaSeleccionada == null) {
            return;
        }
        datePicker1.setDate(placaSeleccionada.getFechaEmision());
        jTextField7.setText(placaSeleccionada.getEstado());
        jTextField9.setText(auto.getNumeroPuertas());
        jTextField10.setText(auto.getTipoMotor());
    }

    private void actualizarEstado() {
        Placa estadoPlaca = (Placa) jComboBox1.getSelectedItem();
        if (estadoPlaca == null) {
            return;
        }
        jTextField7.setText(estadoPlaca.getEstado());
    }

    private void cargarPersonasConLicenciaVigente() {
        jComboBox1.removeAllItems();
        List<Persona> pers = new ArrayList<>();
        try {
            pers = p.ConsultarTodos();
        } catch (Exception ex) {
            Logger.getLogger(LicenciasForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        Set<Long> idsAgregados = new HashSet<>();
        LocalDate hoy = LocalDate.now();
        for (Persona persona : pers) {
            if (!persona.getTramites().isEmpty()) {
                boolean tieneVigente = persona.getTramites().stream().anyMatch(tramite -> {
                    if (tramite instanceof Licencia) {
                        Licencia lic = (Licencia) tramite;
                        LocalDate fechaVigencia = lic.getFechaExpedicion().plusYears(lic.getVigencia());
                        return fechaVigencia.isAfter(hoy) || fechaVigencia.isEqual(hoy);
                    }
                    return false;
                });

                if (tieneVigente && !idsAgregados.contains(persona.getId())) {
                    jComboBox3.addItem(persona);
                    idsAgregados.add(persona.getId());
                }
            }
        }
    }
