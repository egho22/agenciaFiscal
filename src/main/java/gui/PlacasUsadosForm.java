    private void btnRegistrarPlacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPlacaActionPerformed
        // TODO add your handling code here:
        try {
            Automovil auto = (Automovil) jComboBox2.getSelectedItem();
            Placa placaU = (Placa) jComboBox1.getSelectedItem();
            String numeroPlaca = jTextField8.getText();
            String marca = jTextField2.getText();
            String linea = jTextField3.getText();
            String color = jTextField1.getText();
            String modelo = jTextField4.getText();
            LocalDate fechaEmision = datePicker1.getDate();
            LocalDate fechaRecepcion = datePicker2.getDate();
            String estado = jTextField7.getText();
            Persona personaSeleccionada = (Persona) jComboBox3.getSelectedItem();
            if (auto == null || placaU == null || marca.isEmpty() || linea.isEmpty() || color.isEmpty() || modelo.isEmpty() || fechaEmision == null || estado.isEmpty() || fechaRecepcion == null) {
                JOptionPane.showMessageDialog(this, "No puede haber datos vacíos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!numeroPlaca.matches("^[A-Z]{3}-\\d{3}$")) {
                JOptionPane.showMessageDialog(this, "El formato de la placa es inválido.\nDebe ser 3 letras mayúsculas, un guión y 3 números (ej. AAA-111).", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Placa placaExistente = placa.ConsultarPorNumPlaca(numeroPlaca);
            if (placaExistente != null) {
                JOptionPane.showMessageDialog(this, "No se puede registrar una placa con el mismo número.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!placaU.getEstado().matches("Activa")) {
                JOptionPane.showMessageDialog(this, "No se puede registrar una placa nueva sobre una placa ya desactivada.\nEscoge la placa activa.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!placaU.getPersona().getId().equals(personaSeleccionada.getId())) {
                JOptionPane.showMessageDialog(this, "La placa no le pertenece a la persona seleccionada.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Placa placaUsado = new Placa();
            placaUsado.setFechaEmision(LocalDate.now());
            placaUsado.setEstado("Activa");
            placaUsado.setNumeroPlaca(numeroPlaca);
            placaUsado.setCosto(Integer.parseInt(jTextField6.getText()));
            placaUsado.setVehiculo(auto);
            placaUsado.setPersona(personaSeleccionada);
            if (placa.Agregar(placaUsado) != true) {
                JOptionPane.showMessageDialog(this, "No se puede registrar la placa.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                placaU.setFechaRecepcion(fechaRecepcion);
                placaU.setEstado("Desactiva");
                placa.Actualizar(placaU.getId(), placaU);
                actualizarAuto();
                JOptionPane.showMessageDialog(this, "Placa nueva registrada.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No puede haber datos vacíos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarPlacaActionPerformed

